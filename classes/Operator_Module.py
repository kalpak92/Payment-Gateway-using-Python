class Operator:
    def __init__(self):
        self.__op_id=None
        self.__op_name=None

    def get_op_id(self):
        return self.__op_id


    def get_op_name(self):
        return self.__op_name


    def set_op_id(self, value):
        self.__op_id = value


    def set_op_name(self, value):
        self.__op_name = value

